#include <iostream>
using namespace std;

// cplusplus ref: Operators
// url: http://www.cplusplus.com/doc/tutorial/operators/

// Compound Assignment
// +=
// -=
// *=
// /=
// %=
// &=
// |=
// <<=
// >>=
// ^=

int main() {
    int sum = 10;
    int x = 5;
    int y = 1;
    sum += x;
    cout << sum << endl;

    y += 1;
    cout << y << endl;
    return 0;
}
